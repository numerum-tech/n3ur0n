/**
 * @param {string} name
 * @param {{ size?: number, className?: string, title?: string }} [opts]
 */
export function iconHtml(name, opts = {}) {
    const size = opts.size ?? 20;
    const cls = ["icon", opts.className].filter(Boolean).join(" ");
    const body = ICONS[name];
    if (!body) {
        return `<svg class="${cls}" width="${size}" height="${size}" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round" aria-hidden="true"><circle cx="12" cy="12" r="2"/></svg>`;
    }
    const title = opts.title ? `<title>${opts.title}</title>` : "";
    return `<svg class="${cls}" width="${size}" height="${size}" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round" aria-hidden="true">${title}${body}</svg>`;
}

/** Replace [data-icon] placeholders with inline SVG. */
export function applyIcons(root = document) {
    root.querySelectorAll("[data-icon]").forEach(el => {
        const name = el.dataset.icon;
        if (!name) return;
        const size = Number(el.dataset.iconSize) || (el.classList.contains("rail-btn") ? 20 : 18);
        el.innerHTML = iconHtml(name, { size });
        el.removeAttribute("data-icon");
    });
}

/** Pick an icon name from a MIME type string. */
export function mimeIcon(mime) {
    const m = (mime || "").toLowerCase();
    if (m.startsWith("image/")) return "image";
    if (m.startsWith("audio/")) return "music";
    if (m.startsWith("video/")) return "video";
    if (m === "application/pdf" || m.startsWith("text/") || m.includes("json") || m.includes("document")) {
        return "file-text";
    }
    return "file";
}
