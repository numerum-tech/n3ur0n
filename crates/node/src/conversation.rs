//! Conversation state: in-memory shape of a persisted thread.
//!
//! `ConversationState` is the runtime view of `conversation_turns` rows.
//! It feeds the planner (`to_chat_messages`) and is reconstructed from
//! storage on cache miss or restart.

use n3ur0n_core::InstanceId;
use n3ur0n_storage::Db;
use n3ur0n_storage::conversations::{self, ConversationRecord, TurnRecord};
use serde::{Deserialize, Serialize};
use serde_json::{Value, json};
use thiserror::Error;
use time::OffsetDateTime;
use uuid::Uuid;

/// Errors specific to conversation state load / persist.
#[derive(Debug, Error)]
pub enum ConversationError {
    #[error("storage: {0}")]
    Storage(#[from] n3ur0n_storage::StorageError),

    #[error("serde: {0}")]
    Serde(#[from] serde_json::Error),

    #[error("conversation not found: {0}")]
    NotFound(String),

    #[error("ownership mismatch")]
    OwnershipMismatch,
}

pub type ConversationResult<T> = Result<T, ConversationError>;

/// File attached to a user message (blob protocol class D → A on send).
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq, Eq)]
pub struct UserAttachment {
    pub hash: String,
    pub mime: String,
    pub size: u64,
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub name: Option<String>,
}

/// Incoming user message from the conversations API.
#[derive(Debug, Clone, Default)]
pub struct UserInput {
    pub text: String,
    pub attachments: Vec<UserAttachment>,
}

impl From<String> for UserInput {
    fn from(text: String) -> Self {
        Self {
            text,
            attachments: Vec::new(),
        }
    }
}

impl From<&str> for UserInput {
    fn from(text: &str) -> Self {
        Self {
            text: text.to_string(),
            attachments: Vec::new(),
        }
    }
}

impl UserInput {
    pub fn is_empty(&self) -> bool {
        self.text.trim().is_empty() && self.attachments.is_empty()
    }

    /// Text passed to the planner / LLM (attachments summarised inline).
    pub fn planner_text(&self) -> String {
        let mut text = self.text.trim().to_string();
        if text.is_empty() && !self.attachments.is_empty() {
            text = format!("The user sent {} file(s).", self.attachments.len());
        }
        for att in &self.attachments {
            let label = att.name.as_deref().unwrap_or(&att.mime);
            text.push_str(&format!(
                "\n[attachment: {label} · {} · {} bytes]",
                att.hash, att.size
            ));
        }
        text
    }
}

/// One step in a conversation.
#[derive(Debug, Clone, Serialize, Deserialize)]
#[serde(tag = "role", rename_all = "snake_case")]
pub enum Turn {
    User {
        content: String,
        #[serde(default, skip_serializing_if = "Vec::is_empty")]
        attachments: Vec<UserAttachment>,
        ts: i64,
    },
    Assistant {
        content: String,
        #[serde(default, skip_serializing_if = "Option::is_none")]
        model: Option<String>,
        ts: i64,
    },
    ToolCall {
        /// Free-form id linking to its result. Generated by the planner.
        id: String,
        peer_id: InstanceId,
        capability: String,
        args: Value,
        ts: i64,
    },
    ToolResult {
        /// Matches the `id` of its `ToolCall`.
        call_id: String,
        peer_id: InstanceId,
        capability: String,
        #[serde(default, skip_serializing_if = "Option::is_none")]
        result: Option<Value>,
        #[serde(default, skip_serializing_if = "Option::is_none")]
        error: Option<String>,
        ts: i64,
    },
    System {
        content: String,
        ts: i64,
    },
}

impl Turn {
    pub fn role_label(&self) -> &'static str {
        match self {
            Turn::User { .. } => "user",
            Turn::Assistant { .. } => "assistant",
            Turn::ToolCall { .. } => "tool_call",
            Turn::ToolResult { .. } => "tool_result",
            Turn::System { .. } => "system",
        }
    }

    pub fn ts(&self) -> i64 {
        match self {
            Turn::User { ts, .. }
            | Turn::Assistant { ts, .. }
            | Turn::ToolCall { ts, .. }
            | Turn::ToolResult { ts, .. }
            | Turn::System { ts, .. } => *ts,
        }
    }
}

/// In-memory runtime view of a conversation.
#[derive(Debug, Clone)]
pub struct ConversationState {
    pub id: String,
    pub client_id: String,
    pub title: Option<String>,
    pub created_at: i64,
    pub updated_at: i64,
    pub turns: Vec<Turn>,
    /// Highest `seq` currently persisted; the next append uses `next_seq + 1`.
    next_seq: i64,
}

impl ConversationState {
    pub fn new(id: String, client_id: String, title: Option<String>) -> Self {
        let now = OffsetDateTime::now_utc().unix_timestamp();
        Self {
            id,
            client_id,
            title,
            created_at: now,
            updated_at: now,
            turns: Vec::new(),
            next_seq: 0,
        }
    }

    pub fn next_seq(&self) -> i64 {
        self.next_seq + 1
    }

    /// Append a turn in memory and bookkeep `next_seq`.
    pub fn push(&mut self, turn: Turn) {
        self.next_seq += 1;
        self.updated_at = turn.ts();
        self.turns.push(turn);
    }

    /// Record a User turn (text only).
    pub fn push_user(&mut self, content: impl Into<String>) -> &Turn {
        self.push_user_input(&UserInput {
            text: content.into(),
            attachments: Vec::new(),
        })
    }

    /// Record a User turn with optional attachments.
    pub fn push_user_input(&mut self, input: &UserInput) -> &Turn {
        let ts = OffsetDateTime::now_utc().unix_timestamp();
        self.push(Turn::User {
            content: input.text.clone(),
            attachments: input.attachments.clone(),
            ts,
        });
        self.turns.last().expect("just pushed")
    }

    /// Record an Assistant turn.
    pub fn push_assistant(&mut self, content: String, model: Option<String>) -> &Turn {
        let ts = OffsetDateTime::now_utc().unix_timestamp();
        self.push(Turn::Assistant { content, model, ts });
        self.turns.last().expect("just pushed")
    }

    /// Record a ToolCall and return its generated id.
    pub fn push_tool_call(
        &mut self,
        peer_id: InstanceId,
        capability: String,
        args: Value,
    ) -> String {
        let id = format!("call_{}", Uuid::new_v4().simple());
        self.push_tool_call_with_id(id.clone(), peer_id, capability, args);
        id
    }

    /// Record a ToolCall reusing a caller-provided id. Used when the plan
    /// executor already minted the id (so the in-memory turn and the row it
    /// persisted incrementally to the DB share the same `call_id`).
    pub fn push_tool_call_with_id(
        &mut self,
        id: String,
        peer_id: InstanceId,
        capability: String,
        args: Value,
    ) {
        let ts = OffsetDateTime::now_utc().unix_timestamp();
        self.push(Turn::ToolCall {
            id,
            peer_id,
            capability,
            args,
            ts,
        });
    }

    pub fn push_tool_result(
        &mut self,
        call_id: String,
        peer_id: InstanceId,
        capability: String,
        result: Option<Value>,
        error: Option<String>,
    ) {
        let ts = OffsetDateTime::now_utc().unix_timestamp();
        self.push(Turn::ToolResult {
            call_id,
            peer_id,
            capability,
            result,
            error,
            ts,
        });
    }

    pub fn push_system(&mut self, content: String) {
        let ts = OffsetDateTime::now_utc().unix_timestamp();
        self.push(Turn::System { content, ts });
    }

    /// Convert turns to OpenAI-style chat messages, applying:
    /// 1. Visibility filter: hide ToolCall/ToolResult turns that belong to a
    ///    completed dispatch (i.e. happened before the most recent Assistant
    ///    turn). The planner sees a clean User/Assistant trail for past
    ///    rounds; tool turns from the **current** dispatch (after the most
    ///    recent Assistant) stay visible so the planner can iterate.
    ///    Without this, llama3.1:8b and similar imitate past tool_call
    ///    formats inside their content output, producing JSON garbage.
    /// 2. Context window pruning: keep at most `max_turns`, never split a
    ///    ToolCall/ToolResult pair.
    pub fn to_chat_messages(&self, max_turns: usize) -> Vec<Value> {
        let visible = filter_visible_for_llm(&self.turns);
        let pruned = prune_context_refs(&visible, max_turns);
        pruned.into_iter().map(turn_to_message).collect()
    }

    /// Turns expected by the UI (raw representation).
    pub fn ui_turns(&self) -> Vec<Value> {
        self.turns
            .iter()
            .map(|t| serde_json::to_value(t).expect("Turn serialises"))
            .collect()
    }

    /// Dangling tool_call recovery: if the last turn is a ToolCall without a
    /// paired ToolResult, inject a synthetic ToolResult error so the planner
    /// sees a coherent state on resume after a crash.
    pub fn validate(&mut self) {
        let needs_repair = matches!(self.turns.last(), Some(Turn::ToolCall { .. }));
        if needs_repair
            && let Some(Turn::ToolCall {
                id,
                peer_id,
                capability,
                ..
            }) = self.turns.last().cloned()
        {
            self.push_tool_result(
                id,
                peer_id,
                capability,
                None,
                Some("instance restarted before result was persisted".into()),
            );
        }
    }
}

/// Convert a Turn to an OpenAI-shaped chat message.
///
/// Tool names use the **short peer prefix** (12 chars after `n3:`) to match
/// what the planner advertises in its `tools` array. Mixing full and short
/// peer ids inside the same conversation confuses LLMs.
fn turn_to_message(turn: &Turn) -> Value {
    match turn {
        Turn::User { content, .. } => json!({"role": "user", "content": content}),
        Turn::Assistant { content, .. } => json!({"role": "assistant", "content": content}),
        Turn::System { content, .. } => json!({"role": "system", "content": content}),
        Turn::ToolCall {
            id,
            peer_id,
            capability,
            args,
            ..
        } => json!({
            "role": "assistant",
            "content": Value::Null,
            "tool_calls": [{
                "id": id,
                "type": "function",
                "function": {
                    "name": tool_name(peer_id.as_str(), capability),
                    "arguments": serde_json::to_string(args).unwrap_or_else(|_| "{}".into())
                }
            }]
        }),
        Turn::ToolResult {
            call_id,
            result,
            error,
            ..
        } => {
            let content = if let Some(err) = error {
                json!({"error": err}).to_string()
            } else {
                serde_json::to_string(result.as_ref().unwrap_or(&Value::Null))
                    .unwrap_or_else(|_| "null".into())
            };
            json!({
                "role": "tool",
                "tool_call_id": call_id,
                "content": content
            })
        }
    }
}

/// Tool name advertised to the LLM. Must match `Catalog::tool_name`.
pub(crate) fn tool_name(peer_id: &str, capability: &str) -> String {
    let trimmed = peer_id.strip_prefix("n3:").unwrap_or(peer_id);
    let short: String = trimmed.chars().take(12).collect();
    format!("{short}::{capability}")
}

/// Prune context: keep at most `max_turns` from the tail, never splitting a
/// ToolCall / ToolResult pair. v0.1: doesn't preserve leading systems
/// separately — system prompts are injected fresh by the planner at each
/// dispatch, so only persisted Turns matter here.
pub(crate) fn prune_context_refs<'a>(refs: &[&'a Turn], max_turns: usize) -> Vec<&'a Turn> {
    if refs.len() <= max_turns {
        return refs.to_vec();
    }
    let mut start = refs.len() - max_turns;
    // If we'd start at a ToolResult whose ToolCall is just outside, rewind.
    if start > 0
        && let Some(Turn::ToolResult { call_id, .. }) = refs.get(start).copied()
        && matches!(refs.get(start - 1).copied(), Some(Turn::ToolCall { id, .. }) if id == call_id)
    {
        start -= 1;
    }
    refs[start..].to_vec()
}

/// Hide ToolCall/ToolResult turns that belong to a **completed** dispatch.
/// A dispatch is "completed" when an Assistant turn closes it.
///
/// Why: when the LLM sees prior tool_calls in its history, it tends to
/// imitate them in raw text content on subsequent turns (observed with
/// llama3.1:8b after a chat tool exchange). Hiding them yields a clean
/// User/Assistant trail for past rounds while keeping the tool turns of
/// the **current** dispatch (those after the most recent Assistant)
/// visible so the planner can iterate.
pub(crate) fn filter_visible_for_llm(turns: &[Turn]) -> Vec<&Turn> {
    let last_assistant_idx = turns
        .iter()
        .enumerate()
        .rev()
        .find(|(_, t)| matches!(t, Turn::Assistant { .. }))
        .map(|(i, _)| i);

    turns
        .iter()
        .enumerate()
        .filter_map(|(i, t)| {
            let is_tool = matches!(t, Turn::ToolCall { .. } | Turn::ToolResult { .. });
            let is_past = last_assistant_idx.map(|li| i <= li).unwrap_or(false);
            if is_tool && is_past { None } else { Some(t) }
        })
        .collect()
}

