/// Create a fresh conversation row + return the new state.
pub fn create(
    db: &Db,
    client_id: &str,
    title: Option<String>,
) -> ConversationResult<ConversationState> {
    let id = format!("conv_{}", Uuid::new_v4().simple());
    let state = ConversationState::new(id.clone(), client_id.to_string(), title.clone());
    conversations::insert(
        db,
        &ConversationRecord {
            id,
            client_id: client_id.to_string(),
            title,
            created_at: state.created_at,
            updated_at: state.updated_at,
        },
    )?;
    Ok(state)
}

/// Load conversation header + all turns, verify ownership, run validate().
pub fn load(db: &Db, id: &str, client_id: &str) -> ConversationResult<ConversationState> {
    let record =
        conversations::get(db, id)?.ok_or_else(|| ConversationError::NotFound(id.to_string()))?;
    if record.client_id != client_id {
        return Err(ConversationError::OwnershipMismatch);
    }
    let raw = conversations::load_turns(db, id)?;
    let mut turns = Vec::with_capacity(raw.len());
    let mut max_seq = 0;
    for r in raw {
        let turn: Turn = serde_json::from_str(&r.payload)?;
        turns.push(turn);
        if r.seq > max_seq {
            max_seq = r.seq;
        }
    }
    let mut state = ConversationState {
        id: record.id,
        client_id: record.client_id,
        title: record.title,
        created_at: record.created_at,
        updated_at: record.updated_at,
        turns,
        next_seq: max_seq,
    };
    state.validate();
    Ok(state)
}

/// Persist the most recently appended turn.
pub fn persist_last(db: &Db, state: &ConversationState) -> ConversationResult<()> {
    let last_idx = state
        .turns
        .len()
        .checked_sub(1)
        .ok_or_else(|| ConversationError::NotFound("empty turns".into()))?;
    let turn = &state.turns[last_idx];
    let payload = serde_json::to_string(turn)?;
    conversations::append_turn(
        db,
        &TurnRecord {
            conversation_id: state.id.clone(),
            seq: state.next_seq, // already incremented at push()
            role: turn.role_label().to_string(),
            payload,
            created_at: turn.ts(),
        },
        state.updated_at,
    )?;
    Ok(())
}

/// Persist one (ToolCall, ToolResult) pair directly to the DB at a seq
/// reserved by the step's plan index — without touching any in-memory
/// `ConversationState`. Used by the plan executor's durability hook so each
/// step's turns hit storage the moment it completes; a crash mid-run then
/// leaves a partial but correctly-ordered trace.
///
/// `base_seq` is the user turn's seq. Step `plan_index` claims
/// `base_seq + 1 + 2*plan_index` for its call and the next seq for its result,
/// matching the order an in-memory replay (user turn, then tool pairs in plan
/// order) would assign.
#[allow(clippy::too_many_arguments)]
pub fn persist_tool_pair_at(
    db: &Db,
    conversation_id: &str,
    base_seq: i64,
    plan_index: usize,
    call_id: &str,
    peer_id: &InstanceId,
    capability: &str,
    args: &Value,
    result: &Option<Value>,
    error: &Option<String>,
    updated_at: i64,
) -> ConversationResult<()> {
    let call_seq = base_seq + 1 + 2 * plan_index as i64;
    let result_seq = call_seq + 1;
    let call = Turn::ToolCall {
        id: call_id.to_string(),
        peer_id: peer_id.clone(),
        capability: capability.to_string(),
        args: args.clone(),
        ts: updated_at,
    };
    let res = Turn::ToolResult {
        call_id: call_id.to_string(),
        peer_id: peer_id.clone(),
        capability: capability.to_string(),
        result: result.clone(),
        error: error.clone(),
        ts: updated_at,
    };
    for (seq, turn) in [(call_seq, call), (result_seq, res)] {
        let payload = serde_json::to_string(&turn)?;
        conversations::append_turn(
            db,
            &TurnRecord {
                conversation_id: conversation_id.to_string(),
                seq,
                role: turn.role_label().to_string(),
                payload,
                created_at: updated_at,
            },
            updated_at,
        )?;
    }
    Ok(())
}

#[cfg(test)]
mod tests {
    use super::*;
    use n3ur0n_core::Keypair;
    use n3ur0n_storage::open_in_memory;

    fn id() -> InstanceId {
        Keypair::generate().instance_id()
    }

    #[test]
    fn create_load_round_trip() {
        let db = open_in_memory().unwrap();
        let mut state = create(&db, "alice", Some("hello".into())).unwrap();
        state.push_user("hi");
        persist_last(&db, &state).unwrap();
        state.push_assistant("hey there".into(), Some("test-model".into()));
        persist_last(&db, &state).unwrap();

        let reloaded = load(&db, &state.id, "alice").unwrap();
        assert_eq!(reloaded.turns.len(), 2);
        assert_eq!(reloaded.next_seq, 2);
    }

    #[test]
    fn ownership_mismatch_rejected() {
        let db = open_in_memory().unwrap();
        let state = create(&db, "alice", None).unwrap();
        let err = load(&db, &state.id, "bob").unwrap_err();
        matches!(err, ConversationError::OwnershipMismatch);
    }

    #[test]
    fn validate_repairs_dangling_tool_call() {
        let db = open_in_memory().unwrap();
        let mut state = create(&db, "alice", None).unwrap();
        state.push_user("call something");
        persist_last(&db, &state).unwrap();
        let _call_id = state.push_tool_call(id(), "echo".into(), json!({}));
        persist_last(&db, &state).unwrap();
        // Don't persist a tool_result — simulate crash.

        let reloaded = load(&db, &state.id, "alice").unwrap();
        // validate() must have appended a synthetic ToolResult.
        assert!(matches!(
            reloaded.turns.last(),
            Some(Turn::ToolResult { error: Some(_), .. })
        ));
    }

    #[test]
    fn pruning_keeps_pairs() {
        let peer = id();
        let mut state = ConversationState::new("c".into(), "alice".into(), None);
        state.push_user("hi");
        let cid = state.push_tool_call(peer.clone(), "x".into(), json!({}));
        state.push_tool_result(cid, peer, "x".into(), Some(json!("ok")), None);
        state.push_assistant("done".into(), None);
        // 4 turns; ask for 2.
        let pruned = state.to_chat_messages(2);
        // Should keep the last 2, both atomic.
        assert!(!pruned.is_empty());
    }

    #[test]
    fn to_chat_messages_emits_tool_messages_during_current_dispatch() {
        // Mid-dispatch: User just pushed, ToolCall + ToolResult appended,
        // no Assistant yet. The LLM must still see the tool_call/result it
        // just executed.
        let peer = id();
        let mut state = ConversationState::new("c".into(), "alice".into(), None);
        state.push_user("u1");
        let cid = state.push_tool_call(peer.clone(), "echo".into(), json!({"x": 1}));
        state.push_tool_result(cid, peer, "echo".into(), Some(json!({"x": 1})), None);
        let msgs = state.to_chat_messages(10);
        let roles: Vec<&str> = msgs.iter().map(|m| m["role"].as_str().unwrap()).collect();
        assert_eq!(roles, ["user", "assistant", "tool"]); // tool_call as assistant + tool_result as tool
    }

    #[test]
    fn past_dispatch_tool_turns_filtered_out() {
        // After Assistant closes a dispatch, its tool_call/tool_result
        // become invisible to subsequent LLM iterations.
        let peer = id();
        let mut state = ConversationState::new("c".into(), "alice".into(), None);
        state.push_user("u1");
        let cid = state.push_tool_call(peer.clone(), "echo".into(), json!({"x": 1}));
        state.push_tool_result(cid, peer, "echo".into(), Some(json!({"x": 1})), None);
        state.push_assistant("first reply".into(), None);
        state.push_user("u2");
        // Mid-second-dispatch: no new tool_call yet.
        let msgs = state.to_chat_messages(10);
        let roles: Vec<&str> = msgs.iter().map(|m| m["role"].as_str().unwrap()).collect();
        // Past dispatch's tool_call+tool_result hidden. Past User+Assistant
        // kept. Current dispatch's User kept.
        assert_eq!(roles, ["user", "assistant", "user"]);
    }

    #[test]
    fn tool_name_uses_short_peer_format() {
        let peer = InstanceId::parse("n3:abcdef1234567890longerthantwelvechars")
            .unwrap_or_else(|_| Keypair::generate().instance_id());
        let mut state = ConversationState::new("c".into(), "alice".into(), None);
        state.push_user("u1");
        let cid = state.push_tool_call(peer.clone(), "chat".into(), json!({}));
        state.push_tool_result(cid, peer, "chat".into(), Some(json!("ok")), None);
        let msgs = state.to_chat_messages(10);
        // The middle message is the tool_call rendered as assistant + tool_calls.
        let tc = &msgs[1]["tool_calls"][0]["function"]["name"];
        let name = tc.as_str().unwrap();
        assert!(!name.starts_with("n3:"));
        assert!(name.ends_with("::chat"));
        // Short part = 12 chars max.
        let prefix = name.split("::").next().unwrap();
        assert!(prefix.len() <= 12);
    }
}
